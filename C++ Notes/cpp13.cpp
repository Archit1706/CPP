#include <iostream>
#include <cmath>
using namespace std;

int main() {
    cout << "Math Functions" << endl;
    cout << max(2, 10) << endl;
    cout << min(2, 10) << endl;

    cout << sqrt(64) << endl;
    cout << log(2) << endl;

    cout << round(2.4) << endl;
    cout << floor(2.5) << endl;
    cout << ceil(2.5) << endl;
    cout << round(-2.8) << endl;
    cout << floor(-2.5) << endl;
    cout << ceil(-2.5) << endl;

    cout << abs(120) << endl;
    cout << abs(-42) << endl;
    cout << abs(-42.24) << endl;

    cout << exp(0) << endl;
    cout << exp(1) << endl;
    cout << exp(2) << endl;
    cout << exp(3) << endl;

    cout << pow(2, 3) << endl;
    cout << pow(3, 2) << endl;

    return 0;
} 